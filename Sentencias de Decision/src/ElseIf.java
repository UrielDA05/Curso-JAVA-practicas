public class ElseIf {
    public static void main(String[] args) {
        // Sentencias de Decicion if/Else
        var llueve = true;
        if (llueve){
            System.out.println("Llevar paraguas");
        } // Se puede quitar llaves si se cumple con una linea de codigo
        else {
            System.out.println("Dejar paraguas en casa");
        }
    }
}
