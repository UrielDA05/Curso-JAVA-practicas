public class OperadoresAsignacion {
    public static void main(String[] args) {
        // Asignacion =
        var numero = 10;
        int numero2; // Declaracion
        numero2 = 5; // Asignacion de valor
        // Asignacion compuesto
        // +=
        numero += 5; // numero = numero + 5
        System.out.println("numero = " + numero);
        // -=, *=, /=, >=
        numero *= 2;
        System.out.println("numero = " + numero);
    }
}
